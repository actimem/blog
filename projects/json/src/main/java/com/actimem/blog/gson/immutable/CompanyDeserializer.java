package com.actimem.blog.gson.immutable;


import com.google.gson.JsonDeserializationContext;
import com.google.gson.JsonDeserializer;
import com.google.gson.JsonElement;
import com.google.gson.JsonObject;
import com.google.gson.JsonParseException;

import java.lang.reflect.Type;

public class CompanyDeserializer implements JsonDeserializer<Company> {
    @Override
    public Company deserialize(JsonElement json, Type typeOfT, JsonDeserializationContext context) throws JsonParseException {
        JsonObject jsonObject = json.getAsJsonObject();
        String name = jsonObject.get("name").getAsString();
        int foundedYear = jsonObject.get("foundedYear").getAsInt();
        double shareValue = jsonObject.get("shareValue").getAsDouble();
        return new Company(name, foundedYear, shareValue);
    }
}
